    def calculate_by_index(self, aspect_i, dry_bulb_i, elev_i, month_i, rh_i, shading_i, slope_i, time_i):
        self.reference_moisture_ = -0.01
        self.correction_moisture_ = -0.01
        self.fine_dead_fuel_moisture_ = -0.01
        if (0 <= aspect_i < self.NUM_ASPECTS and 0 <= dry_bulb_i < self.NUM_DRY_BULB and
                0 <= elev_i < self.NUM_ELEVATIONS and 0 <= month_i < self.NUM_MONTHS and
                0 <= rh_i < self.NUM_RH and 0 <= shading_i < self.NUM_SHADINGS and
                0 <= slope_i < self.NUM_SLOPES and 0 <= time_i < self.NUM_TIMES_OF_DAY):
            self.reference_moisture_ = self.REFERENCE_MOISTURES[dry_bulb_i][rh_i]
            row = (slope_i + 2 * aspect_i) if shading_i == 0 else (8 + aspect_i)
            row += 12 * month_i
            col = elev_i + 3 * time_i
            self.correction_moisture_ = self.CORRECTION_MOISTURES[row][col]
            self.fine_dead_fuel_moisture_ = self.reference_moisture_ + self.correction_moisture_
